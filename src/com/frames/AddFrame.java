    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
      String studentName = nameField.getText();
      String phone = phoneField.getText();
      String address = addressField.getText();
      String batch = batchCombo.getSelectedItem().toString();
      batchId = batchDAOImpl.getBatchIdByName(batch);
      Batch batch1 = batchDAOImpl.getBatchById(batchId);
      
      Student s = new Student();
      s.setStudentName(studentName);
      s.setStudentBatch(batch1);
      s.setStudentPhone(phone);
      s.setStudentAddress(address);
      
      int result = studentDAOImpl.addStudent(s);
      if(result > 0){
      ErrorLabel.setText("Successfuly added");
      ErrorLabel.setForeground(Color.GREEN);
      }else{
      ErrorLabel.setText("Error");
      ErrorLabel.setForeground(Color.RED);
      }
    }//GEN-LAST:event_addButtonActionPerformed

