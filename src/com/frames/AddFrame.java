    public void fillBatchCombo(){
    List<Batch> allBatches = batchDAOImpl.getAllBatch();
    batchCombo.addItem("Select Batch");
    for(Batch batch:allBatches){
    batchCombo.addItem(batch.getBatchName());
    }
    
    }
    
    
