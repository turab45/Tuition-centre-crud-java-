    private void studentTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_studentTableMouseClicked
        id = (Integer) studentTable.getValueAt(studentTable.getSelectedRow(), 0);
        
        Student s = studentDAOImpl.getStudentById(id);
        idField.setText(String.valueOf(s.getStudentId()));
        nameField.setText(s.getStudentName());
        Batch batch = batchDAOImpl.getBatchById(s.getStudentBatch().getBatchId());
        batchCombo.setSelectedItem(batch.getBatchName());
        phoneField.setText(s.getStudentPhone());
        addressArea.setText(s.getStudentAddress());
    }//GEN-LAST:event_studentTableMouseClicked

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
      
        String name = nameField.getText();
        String batch = batchCombo.getSelectedItem().toString();
        String phone = phoneField.getText();
        String address = addressArea.getText();
        
        Integer b_id = batchDAOImpl.getBatchIdByName(batch);
        Batch sBatch = batchDAOImpl.getBatchById(b_id);
        Student s = studentDAOImpl.getStudentById(id);
        s.setStudentId(id);
        s.setStudentName(name);
        s.setStudentBatch(sBatch);
        s.setStudentPhone(phone);
        s.setStudentAddress(address);
        
        studentDAOImpl.updateStudent(s);
        fillStudentTable();
    }//GEN-LAST:event_updateButtonActionPerformed

