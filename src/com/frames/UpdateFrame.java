    public void fillStudentTable(){
    
        Object columns[]={"ID","Name","Batch","Phone","Address"};
        DefaultTableModel model = new DefaultTableModel(columns, 0);
        List<Student> allStudents = studentDAOImpl.getAllStudent();
        
        for(Student s: allStudents){
        Batch batch = batchDAOImpl.getBatchById(s.getStudentBatch().getBatchId());
            Object rowData[] = {s.getStudentId(),s.getStudentName(),batch.getBatchName(),s.getStudentPhone(),s.getStudentAddress()};
        
            model.addRow(rowData);
            studentTable.setModel(model);
        }
    
    }
    
    

